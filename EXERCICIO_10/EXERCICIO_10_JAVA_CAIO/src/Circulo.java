public class Circulo {
    double raio;
}

/*Explicação: O atributo raio é fundamental porque ele determina as dimensões principais
das formas geométricas. No caso do círculo, o raio define o seu tamanho, influencia
o cálculo do diâmetro e é a base para determinar a sua área. Já na esfera,
o raio é essencial para calcular tanto o volume quanto a área da superfície, pois ambos
dependem diretamente desse valor.*/